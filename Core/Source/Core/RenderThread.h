#pragma once

#include "Thread.h"

namespace Dodo {

    ////////////////////////////////////////////////////////////////
    // THREAD POLICY ///////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////

    enum class ThreadPolicy
    {
        SingleThreaded,
        MultiThreaded ,
        AutoCount     ,
        None
    };

    ////////////////////////////////////////////////////////////////
    // RENDER THREAD STATE /////////////////////////////////////////
    ////////////////////////////////////////////////////////////////

    enum class RenderThreadState
    {
        Idle     ,
        Busy     ,
        Kick     ,
        AutoCount,
        None
    };

    ////////////////////////////////////////////////////////////////
    // RENDER THREAD ///////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////

    class RenderThread
    {
    public:
        RenderThread(ThreadPolicy policy);

        bool IsRunning() const
        {
            return m_IsRunning;
        }

        void Run();

    private:
        ThreadPolicy m_ThreadPolicy = ThreadPolicy::None;
        bool         m_IsRunning    = false;
        Ref<Thread>  m_Thread       = nullptr;
    };
}